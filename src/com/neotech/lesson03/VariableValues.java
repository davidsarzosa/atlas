package com.neotech.lesson03;

public class VariableValues {

	public static void main(String[] args) {

		/*
		 * 2. Create a Java program and name it VariableValues In your program create
		 * variables to store all different types of primitive data. Print the value of
		 * each variable. Update value of each variable. Print the value of each
		 * variable after updating.
		 */

		// THIS ARE VARIABLE VALUES --->

		byte a = 15;
		short b = 130;
		int c = 2500;
		long d = 654321654l;
		float e = 2.654654f;
		double f = 5.6543213524;
		char g = '%';
		boolean h = false;

		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);
		System.out.println(f);
		System.out.println(g);
		System.out.println(h);
		System.out.println();

		// This will be after reassigning values

		a = 80;
		b = 180;
		c = 4500;
		d = 789654354635l;
		e = 2.9873546357f;
		f = 6.654215465;
		g = '^';
		h = true;

		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);
		System.out.println(f);
		System.out.println(g);
		System.out.println(h);
		System.out.println();

	}

}
